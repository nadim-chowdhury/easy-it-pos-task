// Helper function to get products by category
export const getProductsByCategory = (category: any) => {
  return demoProducts.filter(
    (product) => product.category.toLowerCase() === category.toLowerCase()
  );
};

// Helper function to get low stock products (less than 10 items)
export const getLowStockProducts = () => {
  return demoProducts.filter((product) => product.stockQty < 10);
};

// Helper function to get out of stock products
export const getOutOfStockProducts = () => {
  return demoProducts.filter((product) => product.stockQty === 0);
};

// Helper function to get products in a price range
export const getProductsByPriceRange = (minPrice: any, maxPrice: any) => {
  return demoProducts.filter(
    (product) => product.price >= minPrice && product.price <= maxPrice
  );
};

// Summary statistics
export const getProductStats = () => {
  const totalProducts = demoProducts.length;
  const totalValue = demoProducts.reduce(
    (sum, product) => sum + product.price * product.stockQty,
    0
  );
  const categories = [...new Set(demoProducts.map((p) => p.category))];
  const lowStockCount = getLowStockProducts().length;
  const outOfStockCount = getOutOfStockProducts().length;

  return {
    totalProducts,
    totalValue: Math.round(totalValue * 100) / 100,
    totalCategories: categories.length,
    categories,
    lowStockCount,
    outOfStockCount,
    averagePrice:
      Math.round(
        (demoProducts.reduce((sum, p) => sum + p.price, 0) / totalProducts) *
          100
      ) / 100,
  };
};

